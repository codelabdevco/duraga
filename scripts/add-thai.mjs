let content = readFileSync(dataPath, "utf-8");

// Parse JSON array from the file
const startMarker = "export const allTarotCards: TarotCard[] = ";
const startIdx = content.indexOf(startMarker) + startMarker.length;
const endMarker = "\n];\n";
const endIdx = content.indexOf(endMarker, startIdx) + 2;

const jsonStr = content.substring(startIdx, endIdx);
const cards = JSON.parse(jsonStr);

// Add Thai translations to Major Arcana
for (const card of cards) {
  const th = thaiData[card.nameEn];
  if (th) {
    if (th.meaningTh) card.meaningTh = th.meaningTh;
    if (th.analysisTh) card.analysisTh = th.analysisTh;
    if (th.goldenSentenceTh) card.goldenSentenceTh = th.goldenSentenceTh;
  }
}

// Rebuild file
const newContent = content.substring(0, startIdx) + JSON.stringify(cards, null, 2) + ";\n" + content.substring(endIdx + 1);
writeFileSync(dataPath, newContent);

